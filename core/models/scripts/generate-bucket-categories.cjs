#!/usr/bin/env node
/**
 * Generates the VaultDataBucketCategory representation for C#, TypeScript, Swift, and Kotlin.
 *
 * The single source of truth is the Rust codec's `BUCKET_TABLES` static in
 * core/rust/src/vault_codec/types.rs, which owns the manifest-v1 table -> bucket-category mapping
 * shared by every platform. The distinct bucket categories are a projection of that mapping, so we
 * parse them straight out of the Rust source and emit the per-platform category types. 
 */

const fs = require('fs');
const path = require('path');

// Paths
const REPO_ROOT = path.join(__dirname, '../../..');
const RUST_SOURCE = path.join(REPO_ROOT, 'core/rust/src/vault_codec/types.rs');
const CS_OUTPUT = path.join(REPO_ROOT, 'apps/server/Shared/AliasVault.Shared/Models/WebApi/V2/Vault/VaultDataBucketCategory.cs');
const TS_OUTPUT = path.join(REPO_ROOT, 'core/models/src/vault/VaultDataBucketCategory.ts');
const SWIFT_OUTPUT = path.join(REPO_ROOT, 'apps/mobile-app/ios/VaultModels/VaultDataBucketCategory.swift');
const KOTLIN_OUTPUT = path.join(REPO_ROOT, 'apps/mobile-app/android/app/src/main/java/net/aliasvault/app/vaultstore/models/VaultDataBucketCategory.kt');

/**
 * Human-readable description per category, keyed by the category name declared in Rust.
 * Purely presentational (the category set itself always comes from Rust); an unknown category
 * falls back to a generic description so a new bucket never breaks generation.
 */
const CATEGORY_DESCRIPTIONS = {
  Settings: 'User client settings (sort order, autofill prefs, identity defaults, etc.).',
};
const DEFAULT_DESCRIPTION = 'A manifest-v1 data bucket category.';

const AUTOGEN_NOTE = 'core/rust/src/vault_codec/types.rs (BUCKET_TABLES)';
const REGEN_HINT = "Run 'core/models/build.sh' (or 'node core/models/scripts/generate-bucket-categories.cjs') to regenerate.";

/**
 * Parse the distinct bucket categories out of the Rust BUCKET_TABLES static, in declaration order.
 * Each entry is `("Table", "Category")`; the category is the second string literal.
 */
function parseBucketCategories(rustSource) {
  const arrayMatch = rustSource.match(/BUCKET_TABLES\s*:\s*&\[[^\]]*\]\s*=\s*&\[([\s\S]*?)\];/);
  if (!arrayMatch) {
    throw new Error(`Could not find BUCKET_TABLES in ${RUST_SOURCE}`);
  }

  const categories = [];
  const entryRegex = /\(\s*"[^"]*"\s*,\s*"([^"]*)"\s*\)/g;
  let match;
  while ((match = entryRegex.exec(arrayMatch[1])) !== null) {
    const category = match[1];
    if (!categories.includes(category)) {
      categories.push(category);
    }
  }

  if (categories.length === 0) {
    throw new Error('Parsed zero categories from BUCKET_TABLES; the parser or the Rust format changed.');
  }

  return categories;
}

/**
 * Description for a category, falling back to a generic one for categories without an explicit entry.
 */
function describe(category) {
  return CATEGORY_DESCRIPTIONS[category] || DEFAULT_DESCRIPTION;
}

/** Convert a PascalCase category to camelCase (Swift). */
function toCamelCase(name) {
  return name.charAt(0).toLowerCase() + name.slice(1);
}

/** Convert a PascalCase category to SCREAMING_SNAKE_CASE (Kotlin). */
function toScreamingSnakeCase(name) {
  return name.replace(/([A-Z])/g, '_$1').toUpperCase().replace(/^_/, '');
}

/**
 * Generate the C# enum. Serialized as its string name (JsonStringEnumConverter) and stored as a
 * string in the database, so the numeric backing is cosmetic; ordinals follow declaration order.
 */
function generateCSharp(categories) {
  const members = categories
    .map((category, index) => `    /// <summary>${describe(category)}</summary>
    ${category} = ${index},`)
    .join('\n\n');

  return `// <auto-generated />
// This file is auto-generated from ${AUTOGEN_NOTE}.
// Do not edit this file directly. ${REGEN_HINT}

namespace AliasVault.Shared.Models.WebApi.V2.Vault;

using System.Text.Json.Serialization;

/// <summary>
/// Known data-bucket categories for the manifest-v1 storage format. Each value is one small, independently-versioned,
/// user-scoped category of encrypted data kept out of the main vault content manifest so it syncs cheaply.
/// Serialized as its string name on the wire (not the numeric value) and stored as a string in the database.
/// Adding a new kind requires a server-side rollout first, because the server reasons about kinds for
/// per-kind retention policies. The canonical list lives in the Rust codec's BUCKET_TABLES.
/// </summary>
[JsonConverter(typeof(JsonStringEnumConverter))]
public enum VaultDataBucketCategory
{
${members}
}
`;
}

/**
 * Generate the TypeScript const object (consumed as-is by the browser extension and mobile app).
 */
function generateTypeScript(categories) {
  const members = categories
    .map(category => `  /**
   * ${describe(category)}
   */
  ${category}: '${category}',`)
    .join('\n\n');

  return `/**
 * <auto-generated />
 * This file is auto-generated from ${AUTOGEN_NOTE}.
 * Do not edit this file directly. ${REGEN_HINT}
 */

/**
 * Known data-bucket categories for the manifest-v1 storage format. Each value is one small,
 * independently-versioned, user-scoped category of encrypted data kept out of the main vault content
 * manifest so it syncs cheaply. Serialized as its string name on the wire. The canonical list lives in
 * the Rust codec's BUCKET_TABLES.
 */
export const VaultDataBucketCategory = {
${members}
} as const;

/**
 * Type representing all valid vault data bucket category values.
 */
export type VaultDataBucketCategoryValue = typeof VaultDataBucketCategory[keyof typeof VaultDataBucketCategory];
`;
}

/**
 * Generate the Swift string-constant struct (mirrors the FieldType.swift convention).
 */
function generateSwift(categories) {
  const members = categories
    .map(category => `    /// ${describe(category)}
    public static let ${toCamelCase(category)} = "${category}"`)
    .join('\n\n');

  const all = categories.map(toCamelCase).join(', ');

  return `// <auto-generated />
// This file is auto-generated from ${AUTOGEN_NOTE}.
// Do not edit this file directly. ${REGEN_HINT}

import Foundation

/// Known data-bucket categories for the manifest-v1 storage format.
/// The canonical list lives in the Rust codec's BUCKET_TABLES.
public struct VaultDataBucketCategory {
${members}

    /// All known data bucket categories.
    public static let all = [${all}]
}
`;
}

/**
 * Generate the Kotlin string-constant object (mirrors the FieldType.kt convention).
 */
function generateKotlin(categories) {
  const members = categories
    .map(category => `    /**
     * ${describe(category)}
     */
    const val ${toScreamingSnakeCase(category)} = "${category}"`)
    .join('\n\n');

  const all = categories.map(toScreamingSnakeCase).join(', ');

  return `// <auto-generated />
// This file is auto-generated from ${AUTOGEN_NOTE}.
// Do not edit this file directly. ${REGEN_HINT}

package net.aliasvault.app.vaultstore.models

/**
 * Known data-bucket categories for the manifest-v1 storage format.
 * The canonical list lives in the Rust codec's BUCKET_TABLES.
 */
object VaultDataBucketCategory {
${members}

    /**
     * All known data bucket categories.
     */
    val all = listOf(${all})
}
`;
}

/**
 * Ensure the directory for a file path exists.
 */
function ensureDir(filePath) {
  const dir = path.dirname(filePath);
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

/**
 * Main execution.
 */
function main() {
  if (!fs.existsSync(RUST_SOURCE)) {
    throw new Error(`Source file not found: ${RUST_SOURCE}`);
  }

  const rustSource = fs.readFileSync(RUST_SOURCE, 'utf8');
  const categories = parseBucketCategories(rustSource);
  console.log(`Parsed ${categories.length} bucket categorie(s): ${categories.join(', ')}`);

  const outputs = [
    [CS_OUTPUT, generateCSharp(categories)],
    [TS_OUTPUT, generateTypeScript(categories)],
    [SWIFT_OUTPUT, generateSwift(categories)],
    [KOTLIN_OUTPUT, generateKotlin(categories)],
  ];

  for (const [outputPath, content] of outputs) {
    ensureDir(outputPath);
    fs.writeFileSync(outputPath, content, 'utf8');
    console.log(`Generated: ${outputPath}`);
  }

  console.log('\nVaultDataBucketCategory generation complete!');
}

main();
